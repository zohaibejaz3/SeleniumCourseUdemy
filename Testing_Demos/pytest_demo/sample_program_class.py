class Arithmetic:
    def __init__(self, num_a, num_b):
        self.a = num_a
        self.b = num_b

    def add(self):
        return self.a + self.b

    def subtract(self):
        return self.a - self.b

    def multiply(self):
        return self.a * self.b

    def divide(self):
        return self.a / self.b
